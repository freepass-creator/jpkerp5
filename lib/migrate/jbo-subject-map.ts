/**
 * 자금일보 원문 계정과목을 enum 값으로 정규화.
 *  - 매핑에 있으면 매핑값
 *  - 없으면 원문 그대로 보존(고아 데이터 손실 방지 — 드롭다운엔 안 뜨더라도 원문 유지)
 *  - 빈값이면 undefined
 */
export function mapJboSubject(raw: string | undefined | null): string | undefined {
  if (!raw) return undefined;
  const key = raw.trim();
  if (!key) return undefined;
  return JBO_SUBJECT_MAP[key] ?? key;
}
